#import "GT_Settings.h"


@implementation GT_Settings




- (void) dealloc
{
	

}

- (id) initWithJSONDictionary:(NSDictionary *)dic
{
	if(self = [super init])
	{
		[self parseJSONDictionary:dic];
	}
	
	return self;
}

- (void) parseJSONDictionary:(NSDictionary *)dic
{
	// PARSER
	id favorites_ = [dic objectForKey:@"favorites"];
	if([favorites_ isKindOfClass:[NSString class]])
	{
		self.favorites = favorites_;
	}

	
}

@end
